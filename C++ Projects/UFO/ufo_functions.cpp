/*
The end_game function prints out a message at the end of the game. It takes in two string variables,
codeword and answer, and checks if they are equal. If they are equal, 
the user is congratualted and told that they won the game. 
Otherwise, the user is told that the UFO successully abducted another human.
*/
void end_game(string codeword, string answer){
  /*
  This if else statment checks whether the two function parameters are equal.
  If they are equal, the user is congratualted and told that they won the game. 
  Otherwise, the user is told that the UFO successully abducted another human.
  */
  if(answer == codeword){
    cout << "Hooray! You saved the person and earned a medal of honor!" << endl;
  }else{
    cout << "Oh no! The UFO just flew away with another person!" << endl;
  }
} 
/*
The display_status method displays the incorrect guesses and the correct guesses the user has guessed so far.
The method takes in a char vector and a string, iterates through them, and prints out each of their contents.
*/
void display_status(vector<char> incorrect, string answer){
  cout << "Incorrect Guesses: ";
  // This for loop goes through the vector "incorrect" and prints out every element.
    for (int i = 0; i < incorrect.size(); i++){
      cout << incorrect[i];
    }
    cout << "\nCodeword: ";
    // This for loop goes through the string "answer" and prints out every element of answer plus a space between each character.
    for (int i = 0; i < answer.length(); i++){
      cout << answer[i] << " ";
    }
    cout << endl;
}
